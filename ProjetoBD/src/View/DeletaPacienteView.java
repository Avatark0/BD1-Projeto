    private void DeletarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletarButtonActionPerformed
       String deletarPaciente = "DELETE from paciente WHERE fk_pessoa_cpf = '" + CPFTextField.getText() + "';";
        try {
            Connection conexao = new Conexao().getConnection();
            PreparedStatement statementPaciente = conexao.prepareStatement(deletarPaciente);
            statementPaciente.execute();
            statementPaciente.close();
            JOptionPane.showMessageDialog(null, "Deletado com sucesso!", "Cadastro", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(DeletarFuncionarioView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Não foi possível deletar!\n" + ex.toString(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_DeletarButtonActionPerformed

